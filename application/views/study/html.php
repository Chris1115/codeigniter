                        </div>
                        
                        <br><br> 
                        
                        <p>Tag adalah sebuah penanda awalan dan akhiran dari sebuah elemen di HTML. Tag dibuat dengan kurung siku (<span>&lt;</span>...<span>&gt;</span>), lalu di dalamnya berisi nama tag dan kadang juga ditambahkan dengan atribut.
                        </p><br>

                        <p>Tag selalu ditulis berpasangan. Ada tag pembuka dan ada tag penutupnya. Namun, ada juga beberapa tag yang tidak memiliki pasangan penutup. Tag penutup ditulis dengan menambahkan garis miring (/) di depan nama tag
                        </p>
                        
                        <br>
  
                        <div class="container flex-col border-4">
                          <span><span>&lt;</span>!DOCTYPE<span > html</span><span>&gt;</span></span><br>
