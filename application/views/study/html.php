                              <li><code>&lt;lang&gt;</code> Atribut ini digunakan untuk menambahkan  warna, font, ukuran, dan lainnya</li><br>
                              <li><code>&lt;style&gt;</code> untuk mendeklarasikan bahasa halaman Web</li><br>
                            </ul>
          
