                            <br><br> 
                            
                            <p>Atribut adalah kata khusus yang berada di dalam tag pembuka</p>
                            
                            <br><br>
  
                            <ul>
                              <li><code>&lt;a&gt;</code> mendefinisikan hyperlink. Atribut href menentukan URL halaman yang dituju link</li><br>
