                        <li><span><span>&lt;</span>head<span>&gt;</span></span> Elemen untuk membuat judul web</li><br>
                        <li><span><span>&lt;</span>title<span>&gt;</span></span> Elemen bagian dari head yang akan berisi Judul spesifik yang akan ditunjukkan dalam halaman web</li><br>
                        <li><span><span>&lt;</span>body<span>&gt;</span></span> Elemen untuk bagian isi</li><br>
                        <li><span><span>&lt;</span>h1<span>&gt;</span></span> Elemen untuk mendefinisikan besar teks, tersedia dari h1-h6</li><br>
                        <li><span><span>&lt;</span>p<span>&gt;</span></span>Elemen untuk membuat paragraf
                        </li>
